// Javascript is a programming Language .We used to give instructions to the Computer.
// html file is directly connected to browser.Javascript has connection to browser in the form of console(we write javascript code in console).
// But javascript is connected using "script tag" to html file which is already connected to browser.

// Variables: Variables like container to store data.Variables in Javascript can be declared in 4 ways:
// 1.Automatically 2.Using "Var" Keyword 3.Using "let" keyword 4.Using "const" keyword.
//  only a "letter","underscore: or a "$" symbol should be first character.Reserved words cannot be variable names.

// 1.Automatic
age=24;
age=age+1;
console.log(age);

// 2.var: variable can be re-declared & updated.A global scope variable.
var age=10;
var age=20;  
console.log(age)
// 3.let: variable cannot be re-declared but can be updated.A block scope variable.
let name="Tony";
name="Tony stark";
console.log(name);

// 4.const: variable cannot be re-declared or updated.A block scope variable.
const pi=3.14;
console.log(pi);

// undefined: undefined is a non-assigned value of a variable.
// undefined can be applied to "let" keyword not for "const" keyword.
let a;
console.log(a);
a=10;
console.log(a);









